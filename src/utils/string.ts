/**
 * Удаляет все нецифровые символы из строки, оставляя только цифры.
 * Полезно для очистки пользовательского ввода в числовых полях.
 *
 * @param value - исходная строка
 * @returns строка, содержащая только цифры
 */
export const filterDigits = (value: string): string => {
    return value.replace(/\D/g, "");
};